#include <iostream>
using namespace std;

int main ()
{
	int A [3][3] = {{1, 0, 0},{1, 0, -1},{0, 1, -1}};
	int B [3][3] = {{1, 2, 3},{2, 3, 4},{2, 4, 6}};
	int C [3][3] = {0};

	for (int i=0; i<3; ++i)
	{
		for (int j=0; j<3; ++j)
		{
			for (int k=0; k<3; ++k)
			{
				C[i][j]+=(A[i][k]*B[k][j]);
			}
		}
	}

	cout << "["<<A[0][0]<<" "<<A[0][1]<<"  "<<A[0][2]<<"]   ["<<B[0][0]<<" "<<B[0][1]<<" "<<B[0][2]<<"]   [ "<<C[0][0]<<"  "<<C[0][1]<<"  "<<C[0][2]<<"]\n";
	cout << "["<<A[1][0]<<" "<<A[1][1]<<" "<<A[1][2]<<"] * ["<<B[1][0]<<" "<<B[1][1]<<" "<<B[1][2]<<"] = ["<<C[1][0]<<" "<<C[1][1]<<" "<<C[1][2]<<"]\n";
	cout << "["<<A[2][0]<<" "<<A[2][1]<<" "<<A[2][2]<<"]   ["<<B[2][0]<<" "<<B[2][1]<<" "<<B[2][2]<<"]   [ "<<C[2][0]<<" "<<C[2][1]<<" "<<C[2][2]<<"]\n";
	
	system ("pause");
	return 0;
}